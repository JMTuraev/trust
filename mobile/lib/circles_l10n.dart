const Map<String, Map<String, String>> kCircleLangs = {
  'uz': _clUz, 'ru': _clRu, 'en': _clEn, 'es': _clEs, 'fr': _clFr, 'zh': _clZh,
};

/// Joriy til circle-lug'ati (fallback: en).
Map<String, String> cl([String? lang]) =>
    kCircleLangs[lang ?? (store.S['lang'] as String? ?? 'en')] ?? _clEn;

/// Kalitni oling va {token}'larni almashtiring.
String cf(String key, [Map<String, String>? vars]) {
  var s = cl()[key] ?? _clEn[key] ?? key;
  if (vars != null) vars.forEach((k, v) => s = s.replaceAll('{$k}', v));
  return s;
}
